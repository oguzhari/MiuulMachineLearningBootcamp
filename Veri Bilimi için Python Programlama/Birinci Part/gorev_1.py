##########################################################
# Görev 1: Verilen Değerlerin Veri Yapılarını inceleyiniz.

# İpucu: type() methodunu kullanınız.
##########################################################

x = 8
type(x)
# Çıktı: <class 'int'>

y = 3.2
type(y)
# Çıktı: <class 'float'>

z = 8j + 12
type(z)
# Çıktı: <class 'complex'>

a = "Hello World"
type(a)
# Çıktı: <class 'str'>

b = True
type(b)
# Çıktı: <class 'bool'>

c = 23 < 22
type(c)
# Çıktı: <class 'bool'>

le = [1, 2, 3, 4]
type(le)
# Çıktı: <class 'list'>

d = {"Name": "Jake",
     "Age": 27,
     "Address": "Downtown"}
type(d)
# Çıktı: <class 'dict'>

t = {"Machine Learning", "Data Science"}
type(t)
# Çıktı: <class 'set'>

s = {"Python", "Machine Learning", "Data Science"}
type(s)
# Çıktı: <class 'set'>
